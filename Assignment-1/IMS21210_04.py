# Input: Get three integers from the user
first_integer = int(input("Input the first integer: "))
second_integer = int(input("Input the second integer: "))
third_integer = int(input("Input the third integer: "))

# Find the maximum value among the three integers
maximum_value = max(first_integer, second_integer, third_integer)

# Output the result
print(f"Maximum value of three integers: {maximum_value}")
